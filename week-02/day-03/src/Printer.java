public class Printer {
  public static void main(String[] args){
    String a1 = "Hello";
    String a2 = "Dear Mr";
    String a3 = "Kristof Majoros!";
    printer(a1, a2, a3);
  }

  public static void printer(String s1, String s2, String s3) {
    System.out.println(s1 + " " + s2 + " " + s3);
  }
}
